int main()
{
	float fValue = 0.0f;
	double dRet = 0.0;

	printf("Enter radius\n");
	scanf("%f", &fValue);

	dRet = CircleArea(fValue);			// Method Call

	printf("Area of Circle is %.4lf \n", dRet);

	return 0;
}	//End if main

