int KMToMeter(int iNo)
{
	return iNo * 1000;
}	//End of KMToMeter

///////////////////////////////////////////////////////////////////////////////////
//
//	Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	int iRet = 0;

	printf("Enter distance\n");
	scanf("%d", &iValue);

	iRet = KMToMeter(iValue);			// Method Call

	printf("Value in INR is %d \n", iRet);
	return 0;
}	//End if main

