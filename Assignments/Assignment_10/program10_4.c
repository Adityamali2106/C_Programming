double FhtoCs(float fTemp)
{
	double dArea = 0.0;
	
	dArea = ((fTemp-32) * 5 / 9);

	return dArea;

}	//End of FhtoCs

///////////////////////////////////////////////////////////////////////////////////
//
//	Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
	float fValue = 0.0f;
	double dRet = 0.0;

	printf("Enter temperature in Fahrenheit\n");
	scanf("%f", &fValue);

	dRet = FhtoCs(fValue);			// Method Call

	printf("Area of Circle is %.4lf \n", dRet);

	return 0;
}	//End if main

