///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Discription :   It is used to Convert Fahrenheit to celsius
//  Input :         Float
//  Output :        Double
//  Author :        Aditya Mali
//  Date :          28/10/2025
