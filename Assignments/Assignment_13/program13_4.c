#include<stdio.h>                               

///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumNaturalNumbers
//  Discription :   It is used to Calculate sum of N natural Numbers
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          29/10/2025
//
///////////////////////////////////////////////////////////////////////////////////

int SumNaturalNumbers(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        iSum = iSum + iCnt;
    }

    return iSum;

}  //End of SumNaturalNumbers

//  O(N)

///////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    printf("%d\n", SumNaturalNumbers(iValue));

    return 0;
}   //End if main

