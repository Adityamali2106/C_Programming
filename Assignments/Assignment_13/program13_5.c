///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SumEvenNumbers
//  Discription :   It is used to Calculate sum of N natural Numbers
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          29/10/2025
//
///////////////////////////////////////////////////////////////////////////////////

int SumEvenNumbers(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if ((iCnt%2) == 0)
        {
            iSum = iSum + iCnt;
        }
    }

    return iSum;

}  //End of SumEvenNumbers

//  O(N)

///////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    printf("%d\n", SumEvenNumbers(iValue));

    return 0;
}   //End if main

