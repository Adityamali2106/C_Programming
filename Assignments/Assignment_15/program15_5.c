////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountDiff
//  Discription :   It is used to Difference between summation of even digits and sammation of odd digits
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          01/11/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountDiff(int iNo)
{
   int iDigit = 0;
   int iAddDigits1 = 0;
   int iAddDigits2 = 0;

    while(iNo != 0)
    {
        iDigit = iNo % 10;
        if ((iDigit % 2) == 0)
        {
            iAddDigits1 = iAddDigits1 + iDigit;
        }

        if ((iDigit % 2) != 0)
        {
            iAddDigits2 = iAddDigits2 + iDigit;
        }

        iNo = iNo / 10;
    }

    return iAddDigits1 - iAddDigits2;
}  //End of CountDiff

//  O(N)

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    iRet = CountDiff(iValue);

    printf("%d", iRet);
    return 0;
}   //End if main

