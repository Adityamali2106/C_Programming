////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountOdd
//  Discription :   It is used to countOdd the frequency of Odd digits in given Number
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          01/11/2025
//
////////////////////////////////////////////////////////////////////////////////////////////

int CountOdd(int iNo)
{
   int iDigit = 0;
   int iFrequency = 0;

    while(iNo != 0)
    {
        iDigit = iNo % 10;
        if ((iDigit % 2) != 0)
        {
            iFrequency++;
        }

        iNo = iNo / 10;
    }

    return iFrequency;
}  //End of CountOdd

//  O(N)

////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    iRet = CountOdd(iValue);

    printf("%d", iRet);
    return 0;
}   //End if main

