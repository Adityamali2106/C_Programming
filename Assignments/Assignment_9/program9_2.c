int DollarToINR(int iNo)
{
	return iNo * 70;
}	//End of DollarToINR

///////////////////////////////////////////////////////////////////////////////////
//
//	Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	int iRet = 0;

	printf("Enter US Dollar\n");
	scanf("%d", &iValue);

	iRet = DollarToINR(iValue);			// Method Call

	printf("Value in INR is %d \n", iRet);
	return 0;
}	//End if main

