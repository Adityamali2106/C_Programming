///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DollarToINR
//  Discription :   It is used to convert US Dollar to Indian Currency
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          27/10/2025
//
