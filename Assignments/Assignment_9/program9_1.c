////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Discription :   It is used to display Pattern (*  *  *  #  #  #)
//  Input :         Integer
//  Output :        void
//  Author :        Aditya Mali
//  Date :          28/10/2025
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
	int iCnt = 0;									// Counter For loop

	if (iNo < 0)									// Updater
	{
		iNo = -iNo;
	}

	for(iCnt = 1;iCnt <= iNo; iCnt++)
	{
		printf("*\t");
	}

	for(iCnt = 1;iCnt <= iNo; iCnt++)
	{
		printf("#\t");
	}
}	//End of Display

// O(N)

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	Entery point Function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;

	printf("Enter Number\n");						// Accept a Number From user
	scanf("%d", &iValue);


	Display(iValue);								// Method Call

	return 0;
}	// End of main

