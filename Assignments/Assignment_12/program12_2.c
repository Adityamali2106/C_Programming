///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountFactors
//  Discription :   It is used to Count number of factors
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          29/10/2025
//
///////////////////////////////////////////////////////////////////////////////////

int CountFactors(int iNo)
{
    int iCnt = 0;
    int iFactCount = 0;

    if (iNo < 0)
    {
        iNo = -iNo;
    }  

    for(iCnt = 1; iCnt <= (iNo/2); iCnt++)
    {
        if ((iNo% iCnt ) == 0)
        {
            iFactCount++;
        }
    }

    return iFactCount;
}   // End of CountFactors

// O(N/2)

///////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    printf("%d \n",CountFactors(iValue));

    return 0;
}   //End if main

