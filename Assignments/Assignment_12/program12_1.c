void PrintFactors(int iNo)
{
    int iCnt = 0;

    if (iNo < 0)
    {
        iNo = -iNo;
    }  

    for(iCnt = 1; iCnt <= (iNo/2); iCnt++)
    {
        if ((iNo% iCnt ) == 0)
        {
            printf("%d\t",iCnt);
        }
    }
}	// End of PrintFactors

//	O(N/2)

///////////////////////////////////////////////////////////////////////////////////
//
//	Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;

	printf("Enter Number\n");
	scanf("%d", &iValue);

	PrintFactors(iValue);					// Method Call

	return 0;
}	//End if main

