//////////////////////////////////////////////////////////////////////////
//
//  Function Name : Pattern
//  Discription :   It is used to Display Pattern like $	*
//  Input :         Integer
//  Output :        void
//  Author :        Aditya Mali
//  Date :          27/10/2025
//
//////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
	int iCnt = 0;									// Counter For loop

	if (iNo < 0)									// Updater
	{
		iNo = -iNo;
	}

	for(iCnt = 1;iCnt <= iNo; iCnt++)
	{
		printf("$\t*\t");
	}
}	//End of Display

// O(N)

//////////////////////////////////////////////////////////////////////////
//
//	Entery point Function for the application
//
//////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;

	printf("Enter Number\n");						// Accept a Number From user
	scanf("%d", &iValue);


	Pattern(iValue);								// Method Call

	return 0;
}	// End of main

