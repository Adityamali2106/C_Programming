///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Factorial
//  Discription :   It is used to find Factorial of given number
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          27/10/2025
//
///////////////////////////////////////////////////////////////////////////////////

int Factorial(int iNo)
{
	int iCnt = 0;
	int iAns = 0;
	if (iNo < 0)								// Updater
	{
		iNo = -iNo;
	}

	for(iCnt = iNo,iAns = 1;iCnt != 0; iCnt--)
	{
		iAns = iAns * iCnt;
	}

	return iAns;

}	//End of Factorial

///////////////////////////////////////////////////////////////////////////////////
//
//	Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	int iRet = 0;

	printf("Enter Number\n");
	scanf("%d", &iValue);

	iRet = Factorial(iValue);			// Method Call

	printf("Factorial of number is %d\n", iRet);

	return 0;
}	//End if main

