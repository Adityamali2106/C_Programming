int main()
{
	int iValue1 = 0;
	int iValue2 = 0;
	int iResult = 0;

	printf("Enter Two Number\n");
	scanf("%d %d",&iValue1,&iValue2);

	iResult = FindMax(iValue1,iValue2);						// Method call
	
	printf("Maximum is: %d\n", iResult);

	return 0;
}	//End of main

