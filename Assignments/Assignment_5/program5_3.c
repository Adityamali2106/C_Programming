int CheckLeapYear(int iNo)
{
	if (((iNo % 4 == 0) && (iNo % 100 != 0)) || (iNo % 400 == 0))
    {
    	printf("%d is Leep Year\n", iNo);
    }
 	else
 	{
		printf("%d is not Leep Year\n", iNo);
 	} 
}	// End of CheckLeapYear


