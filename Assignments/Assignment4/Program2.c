void FactRev(int iNo)
{
	int iCnt = 0;

	if (iNo < 0)
	{
		iNo = -iNo;
	}

	for ( iCnt = (iNo/2); iCnt >= 1; iCnt--)
	{
		if ((iNo % iCnt) == 0)
		{
			printf("%d\t", iCnt);
		}
	}
}	// End of FactRev

//	O(N/2)

