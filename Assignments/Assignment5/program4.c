void CheckNumberType(int iNo)
{
	if (iNo > 0)
	{
		printf("%d is Positive Number\n", iNo);
	}
	else if(iNo < 0)
	{
		printf("%d is Negative Number\n", iNo);
	}
	else
	{
		printf("%d is Zero \n", iNo);
	}
}	// End of CheckNumberType


