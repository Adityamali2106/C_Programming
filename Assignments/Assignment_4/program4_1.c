//////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	int iRet = 0;

	printf("Enter Number\n");
	scanf("%d",&iValue);

	iRet = MultFact(iValue);						// Method call
	
	printf("%d \n", iRet);

	return 0;
}	//End of main

