#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultFact
//  Discription :   It is used to calculate Multiplication of Factors of given number  
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          24/10/2025
//
///////////////////////////////////////////////////////////////////////////////////////

int MultFact(int iNo)
{
	int iCnt = 0;
	int iMult = 0;

	for ( iCnt = 1, iMult = 1; iCnt <= (iNo/2) ; iCnt++)
	{
		if ((iNo % iCnt) == 0)
		{
			iMult = iMult * iCnt;
		}
	}

	return iMult;
}	// End of MultFact

//	O(N/2)

//////////////////////////////////////////////////////////////////////////
//
//	Entery point Function for the application
