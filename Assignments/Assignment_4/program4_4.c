///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : NonFact
//  Discription :   It is used to summation of all Non factors of given number  
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          24/10/2025
//
///////////////////////////////////////////////////////////////////////////////////////

int SumNonFact(int iNo)
{
	int iCnt = 0;
	int iSum = 0;

	if (iNo < 0)
	{
		iNo = -iNo;
	}

	for ( iCnt = 1; iCnt < iNo; iCnt++)
	{
		if ((iNo % iCnt) != 0)
		{
			iSum = iSum + iCnt;
		}
	}

	return iSum;
}	// End of NonFact

//	O(N)

//////////////////////////////////////////////////////////////////////////
//
//	Entery point Function for the application
//
//////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	int iRet = 0;

	printf("Enter Number\n");
	scanf("%d",&iValue);

	iRet = SumNonFact(iValue);						// Method call

	printf("%d \n", iRet);
	
	return 0;
}	//End of main

