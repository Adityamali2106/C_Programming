//////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Table
//  Discription :   It is used to display table of given number
//  Input :         Integer
//  Output :        void
//  Author :        Aditya Mali
//  Date :          27/10/2025
//
//////////////////////////////////////////////////////////////////////////////////////////

void Table(int iNo)
{
	int iCnt = 0;									// Counter For loop

	if (iNo < 0)									// Updater
	{
		iNo = -iNo;
	}

	for(iCnt = 1;iCnt <= 10; iCnt++)
	{
		printf("%d\t", iNo*iCnt);
	}
}	//End of Table

// O(N)

//////////////////////////////////////////////////////////////////////////////////////////
//
//	Entery point Function for the application
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;

	printf("Enter Number\n");						// Accept a Number From user
	scanf("%d", &iValue);


	Table(iValue);								// Method Call

	return 0;
}	// End of main

