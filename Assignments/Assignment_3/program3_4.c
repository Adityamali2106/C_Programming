//////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayConvert
//  Discription :   It is used to convert case of given character
//  Input :         Character
//  Output :        void
//  Author :        Aditya Mali
//  Date :          24/10/2025
//
//////////////////////////////////////////////////////////////////////////

void DisplayConvert(char cValue)
{
	if (cValue >= 97 && cValue <= 122)								
	{
		printf("%c \n", cValue - 32);
	}
	else if (cValue >= 65 && cValue <= 90)
	{
		printf("%c \n", cValue + 32);
	}
}	//End of DisplayConvert

//////////////////////////////////////////////////////////////////////////
//
//	Entery point Function for the application
//
//////////////////////////////////////////////////////////////////////////

int main()
{
	char cValue = '\0';

	printf("Enter Character\n");							// Accept a character From user
	scanf("%c", &cValue);

	DisplayConvert(cValue);								// Method Call

	return 0;
}	// End of main

