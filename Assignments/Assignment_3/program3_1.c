void PrintEven(int iNo)
{
	int iCnt = 0;

	if (iNo <= 0)									// Check the number is divisible by 2
	{
		return;
	}
	
	for (iCnt = 1; iCnt <= iNo ; iCnt++)
	{
		printf("%d\t", (2 * iCnt));
	}
}	// End of CheckEvenOdd

