bool CheckGreater(int iNo)
{
	if (iNo > 100)								
	{
		return true;
	}
	else
	{
		return false;
	}
}	//End of CheckGreater

///////////////////////////////////////////////////////////////////////////////////
//
//	Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	bool bRet = false;							// To store the result

	printf("Pease Enter Number\n");
	scanf("%d", &iValue);

	bRet = CheckGreater(iValue);				// Method Call

	if (bRet == true)
	{
		printf("Greater\n");
	}
	else
	{
		printf("Smaller\n");
	}

	return 0;
}	//End if main

