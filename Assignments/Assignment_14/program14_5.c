////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Count
//  Discription :   It is used to count the frequency of such digits which are less than 6
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          01/11/2025
//
////////////////////////////////////////////////////////////////////////////////////////////

int Count(int iNo)
{
   int iDigit = 0;
   int iFrequency = 0;

    while(iNo != 0)
    {
        iDigit = iNo % 10;
        if (iDigit < 6)
        {
            iFrequency++;
        }

        iNo = iNo / 10;
    }

    return iFrequency;
}  //End of Count

//  O(N)

////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    iRet = Count(iValue);

    printf("%d", iRet);
    return 0;
}   //End if main

