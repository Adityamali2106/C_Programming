////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountTwo
//  Discription :   It is used to count the frequency of 2 in given number
//  Input :         Integer
//  Output :        Integer
//  Author :        Aditya Mali
//  Date :          01/11/2025
//
////////////////////////////////////////////////////////////////////////////////////////////

int CountTwo(int iNo)
{
   int iDigit = 0;
   int iFrequency = 0;

    while(iNo != 0)
    {
        iDigit = iNo % 10;
        if (iDigit == 2)
        {
            iFrequency++;
        }

        iNo = iNo / 10;
    }

    return iFrequency;
}  //End of CountTwo

//  O(N)

////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    iRet = CountTwo(iValue);

    printf("%d", iRet);
    return 0;
}   //End if main

