////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DispalyDigit
//  Discription :   It is used to display given number in digits in reverse order
//  Input :         Integer
//  Output :        Void
//  Author :        Aditya Mali
//  Date :          01/11/2025
//
////////////////////////////////////////////////////////////////////////////////////////////

void DispalyDigit(int iNo)
{
    int iDigit = 0;

    if (iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo != 0)
    {
        iDigit = iNo % 10;
        printf("%d\n", iDigit);

        iNo = iNo / 10;
    }
}  //End of DispalyDigit

//  O(N)

////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    DispalyDigit(iValue);

    return 0;
}   //End if main

