///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : PrintEvenNumbers
//  Discription :   It is used to print all Even numbers from 1 to N
//  Input :         Integer
//  Output :        Void
//  Author :        Aditya Mali
//  Date :          29/10/2025
//
///////////////////////////////////////////////////////////////////////////////////

void PrintEvenNumbers(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if((iCnt % 2) == 0)
        {
            printf("%d\t", iCnt);
        }
    }

}  //End of PrintEvenNumbers

//  O(N)

///////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    PrintEvenNumbers(iValue);

    return 0;
}   //End if main

