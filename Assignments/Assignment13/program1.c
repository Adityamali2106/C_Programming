///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : PrintNumbers
//  Discription :   It is used to print all numbers from 1 to N
//  Input :         Integer
//  Output :        Void
//  Author :        Aditya Mali
//  Date :          29/10/2025
//
///////////////////////////////////////////////////////////////////////////////////

void PrintNumbers(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("%d\t", iCnt);
    }

}  //End of PrintNumbers

//  O(N)

///////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number\n");
    scanf("%d", &iValue);

    PrintNumbers(iValue);

    return 0;
}   //End if main

