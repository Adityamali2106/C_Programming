///////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : IsDivisibleByFive
//  Discription :   It is used to check Given Number is divisible by 5
//  Input :         Integer
//  Output :        Boolen
//  Author :        Aditya Mali
//  Date :          29/10/2025
//
///////////////////////////////////////////////////////////////////////////////////

bool IsDivisibleByFive(int iNo)
{
	if ((iNo % 5)== 0)
	{
		return true;
	}
	else
	{
		return false;
	}

}	// End of IsDivisibleByFive

///////////////////////////////////////////////////////////////////////////////////
//
//  Entory Point Function for the application
//
///////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;

	printf("Enter number : ");
	scanf("%d", &iValue);

	printf("%s\n", IsDivisibleByFive(iValue) ? "Yes" : "No");

	return 0;
}

